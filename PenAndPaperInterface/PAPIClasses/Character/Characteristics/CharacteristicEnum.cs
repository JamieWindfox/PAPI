namespace PAPI.Character.Characteristics
{
    /// <summary>
    /// The enum that represents a certain characterisitc of a character
    /// </summary>
    public enum CharacteristicEnum
    {
        /// <summary>
        /// Base for strength, toughness, indicator for wound threshold
        /// </summary>
        BRAWN,

        /// <summary>
        /// Base for dexterity, hand-eye coordination and body control
        /// </summary>
        AGILITY,

        /// <summary>
        /// Base for intelligence, education and mental acuity
        /// </summary>
        INTELLECT,

        /// <summary>
        /// Base for crafting, cleverness, creativity and how devious someone is
        /// </summary>
        CUNNING,

        /// <summary>
        /// Base for discipline, self-control, mental fortitude and faith
        /// </summary>
        WILLPOWER,

        /// <summary>
        /// Base for charisma, confidence and force of personality
        /// </summary>
        PRESENCE
    }
}